private void refreshCourseTable() {
  java.util.List<model.Course> courses = courseDAO.getAllCourses();
    
    // Clear the table using our renamed variable
    tableModel.setRowCount(0); 
    
    // Loop through database results and add to table
    for (model.Course c : courses) {
        tableModel.addRow(new Object[]{
            c.getCourseId(), 
            c.getCourseName(), 
            c.getCourseDescription(), 
            c.getCredits()
        });
    }
}

