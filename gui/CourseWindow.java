    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
try {
        String name = txtCourseName.getText().trim();
        String desc = txtDescription.getText().trim();
        String creditsStr = txtCredits.getText().trim();

        if (name.isEmpty() || creditsStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in Name and Credits!");
            return;
        }

        int credits = Integer.parseInt(creditsStr);
        
        // This 'model.Course' now works because the variable is renamed to 'tableModel'
        model.Course c = new model.Course(0, name, desc, credits);
        
        if (courseDAO.addCourse(c)) {
            JOptionPane.showMessageDialog(this, "Course Added Successfully!");
            
            // Reload the table so the new course appears
            refreshCourseTable(); 
            
            // Clear the fields
            txtCourseName.setText("");
            txtDescription.setText("");
            txtCredits.setText("");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Credits must be a number!");
    }
    }//GEN-LAST:event_btnAddCourseActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    int selectedRow = tblCourses.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Select a course to update.");
        return;
    }

    // Convert view index to model index to support sorting/filtering
    int modelIndex = tblCourses.convertRowIndexToModel(selectedRow);
    int id = (int) tableModel.getValueAt(modelIndex, 0);
    String name = txtCourseName.getText().trim();
    String desc = txtDescription.getText().trim();
    int credits = Integer.parseInt(txtCredits.getText().trim());

    model.Course c = new model.Course(id, name, desc, credits);
    if (courseDAO.updateCourse(c)) {
        JOptionPane.showMessageDialog(this, "Course Updated!");
        refreshCourseTable();
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    // 1. Get the selected row from the table
    int selectedRow = tblCourses.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a course to delete!");
        return;
    }

    // 2. Convert index to model index (important if you use the search/sorter)
    int modelIndex = tblCourses.convertRowIndexToModel(selectedRow);
    
    // 3. Get the Course ID from the first column (index 0)
    int courseId = (int) tableModel.getValueAt(modelIndex, 0);

    // 4. Confirm deletion with the user
    int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete this course?", 
            "Confirm Deletion", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        // 5. Call the DAO to delete from the database
        if (courseDAO.deleteCourse(courseId)) {
            JOptionPane.showMessageDialog(this, "Course Deleted Successfully!");
            refreshCourseTable(); // Refresh the GUI table
        } else {
            // This usually triggers if a foreign key constraint is violated
            JOptionPane.showMessageDialog(this, "Error: Could not delete course. It may have enrolled students.");
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

