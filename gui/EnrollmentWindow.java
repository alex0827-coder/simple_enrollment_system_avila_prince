private void refreshEnrollmentTable() {
    try {
        // Change the List type to Object[] to match your DAO's output
        List<Object[]> enrollments = enrollmentDAO.getAllEnrollments();
        
        DefaultTableModel model = (DefaultTableModel) tblEnrollment.getModel();
        model.setRowCount(0); 

        for (Object[] row : enrollments) {
            // Directly add the row provided by the DAO
            // This assumes your DAO returns: [ID, Student Name, Course Name, Date]
            model.addRow(row);
        }
    } catch (Exception ex) {
        logger.log(Level.SEVERE, "Failed to refresh enrollment table", ex);
    }
}

public boolean isAlreadyEnrolled(int studentId, int courseId) {
    String sql = "SELECT COUNT(*) FROM Enrolled_Subject WHERE student_id = ? AND course_id = ?";
    try (Connection conn = DatabaseConnection.getConnection();
         PreparedStatement ps = conn.prepareStatement(sql)) {
        ps.setInt(1, studentId);
        ps.setInt(2, courseId);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            return rs.getInt(1) > 0;
        }
    } catch (SQLException e) {
        System.out.println("Check Enrollment Error: " + e.getMessage());
    }
    return false;
}

