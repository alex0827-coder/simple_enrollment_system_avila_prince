    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
try {
        int sId = Integer.parseInt(cmbStudents.getSelectedItem().toString().split(" - ")[0]);
        int cId = Integer.parseInt(cmbCourses.getSelectedItem().toString().split(" - ")[0]);

        // Use the DAO to check for duplicates
        if (enrollmentDAO.isAlreadyEnrolled(sId, cId)) {
            JOptionPane.showMessageDialog(this, "Student is already enrolled in this course!");
            return; 
        }

        model.Enrollment enrollment = new model.Enrollment(0, sId, cId, new java.sql.Date(System.currentTimeMillis()));
        if (enrollmentDAO.addEnrollment(enrollment)) {
            JOptionPane.showMessageDialog(this, "Enrolled Successfully!");
            refreshEnrollmentTable();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_btnEnrollActionPerformed

