    private void formKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyReleased
    DefaultTableModel obj = (DefaultTableModel) tblStudents.getModel();
    TableRowSorter<DefaultTableModel> sol = new TableRowSorter<>(obj);
    tblStudents.setRowSorter(sol);
    
    // This part does the actual filtering
    String query = txtSearch.getText();
    sol.setRowFilter(RowFilter.regexFilter("(?i)" + query));
    }//GEN-LAST:event_formKeyReleased

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased

    String query = txtSearch.getText().trim();
    if (query.length() == 0) {
        sorter.setRowFilter(null);
    } else {
        // Case-insensitive regex filter
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query)); 
    }


    }//GEN-LAST:event_txtSearchKeyReleased


