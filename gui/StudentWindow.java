private void refreshTable() {
    List<model.Student> list = studentDAO.getAllStudents();
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
    model.setRowCount(0); 
    for (model.Student s : list) {
        model.addRow(new Object[]{s.getStudentId(), s.getFirstName(), s.getLastName(), s.getAge(), s.getEmail()});
    }

}

