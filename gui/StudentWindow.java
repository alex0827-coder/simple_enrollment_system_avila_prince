    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    int selectedRow = tblStudents.getSelectedRow();
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a student to update");
        return;
    }

    // 1. Get the ID from the selected row
    int id = (int) tblStudents.getValueAt(selectedRow, 0);
    
    // 2. Create a Student object with the new text field values
    model.Student s = new model.Student(
        id, 
        txtFirstName.getText(), 
        txtLastName.getText(), 
        Integer.parseInt(txtAge.getText()), 
        txtEmail.getText()
    );

    // 3. Call the DAO to update the database
    if (studentDAO.updateStudent(s)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Student updated successfully!");
        refreshTable(); // Reload the table to show changes
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 String email = txtEmail.getText().trim();
    
    // --- THE GUARD CLAUSE ---
    if (studentDAO.isEmailExists(email)) {
        JOptionPane.showMessageDialog(this, 
            "Error: A student with the email '" + email + "' already exists!", 
            "Duplicate Entry", 
            JOptionPane.ERROR_MESSAGE);
        return; // This stops the code from reaching the addStudent line
    }

    // Only if the check passes, we proceed:
    String firstName = txtFirstName.getText().trim();
    String lastName = txtLastName.getText().trim();
    int age = Integer.parseInt(txtAge.getText().trim());

    model.Student newStudent = new model.Student(0, firstName, lastName, age, email);
    if (studentDAO.addStudent(newStudent)) {
        JOptionPane.showMessageDialog(this, "Student Added!");
        refreshTable();
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    int row = tblStudents.getSelectedRow();
if(row != -1) {
    int id = (int) tblStudents.getValueAt(row, 0);
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?");
    if(confirm == javax.swing.JOptionPane.YES_OPTION) {
        studentDAO.deleteStudent(id);
        refreshTable();
    }
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first.");
}
    
    }//GEN-LAST:event_jButton3ActionPerformed

